    municipality_number INT,
    country_code TEXT REFERENCES nation_ids (nation_id),
    postal_address TEXT CHECK (postal_address != ''),
    postal_postal_code INT,
    postal_municipality_number INT,
    postal_country_code TEXT REFERENCES nation_ids (nation_id),
    latitude DOUBLE PRECISION,
    longitude DOUBLE PRECISION,
    delivery_point_source_id INT NOT NULL DEFAULT (4) REFERENCES delivery_point_sources (delivery_point_source_id) CHECK (delivery_point_source_id = 4)
);

