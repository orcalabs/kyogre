    buyer_location_id BIGINT NOT NULL PRIMARY KEY,
    delivery_point_id TEXT REFERENCES delivery_point_ids (delivery_point_id),
    parent BIGINT REFERENCES buyer_locations (buyer_location_id),
    location_type INT NOT NULL REFERENCES buyer_location_types (buyer_location_type_id),
    legal_entity_id TEXT CHECK (legal_entity_id != ''),
    main_legal_entity_id TEXT CHECK (main_legal_entity_id != ''),
    parent_legal_entity_id TEXT CHECK (parent_legal_entity_id != ''),
    name TEXT CHECK (name != ''),
    created TIMESTAMP NOT NULL,
    updated TIMESTAMP NOT NULL,
    address TEXT CHECK (address != ''),
