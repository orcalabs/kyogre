    delivery_point_id TEXT NOT NULL UNIQUE REFERENCES delivery_point_ids (delivery_point_id),
    buyer_location_id BIGINT NOT NULL UNIQUE REFERENCES buyer_locations (buyer_location_id),
    is_manual BOOLEAN NOT NULL DEFAULT FALSE
